function day1($input) {
  $input = trim($input);
  $lines = explode("\n", $input);
  $res = 0;
  for ($i = 0; $i < sizeof($lines); $i++) {
    if ($lines[$i] == 'A X' || $lines[$i] == 'B Y' || $lines[$i] == 'C Z') {
      $res += 3;
    } else if ($lines[$i] == 'A Y' || $lines[$i] == 'B Z' || $lines[$i] == 'C X') {
      $res += 6;
    }

    $own = explode(' ', $lines[$i])[1];
    switch ($own) {
      case 'X':
        $res += 1;
        break;
      case 'Y':
        $res += 2;
        break;
      case 'Z':
        $res += 3;
    }
  }

  return $res;
}

function day2($input) {
  $input = trim($input);
  $lines = explode("\n", $input);
  $res = 0;
  for ($i = 0; $i < sizeof($lines); $i++) {
    $split = explode(' ', $lines[$i]);
    $opp = $split[0];
    $own = $split[1];
    if (($opp == 'A' && $own == 'Y') || ($opp == 'B' && $own == 'X') || ($opp == 'C' && $own == 'Z')) {
      $res += 1;
    } else if (($opp == 'A' && $own == 'Z') || ($opp == 'B' && $own == 'Y') || ($opp == 'C' && $own == 'X')) {
      $res += 2;
    } else {
      $res += 3;
    }

    switch ($own) {
      case 'Y':
        $res += 3;
        break;
      case 'Z':
        $res += 6;
    }
  }

  return $res;
}

